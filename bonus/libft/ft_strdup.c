#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*res;
	size_t		i;

	i = 0;
	res = (char *) malloc ((ft_strlen (s) + 1) * sizeof (char));
	if (!res)
		return (res);
	while (i < ft_strlen(s))
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
